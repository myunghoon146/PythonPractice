# 숫자 두개 넘겨받아서, 더해서 돌려줘라~ GetPlus

n1=23
n2=38

def GetPlus(n1,n2):
    return n1 + n2
print(GetPlus(23,38))

n1=23
n2=38
def ReturnMinus(n1,n2):
    return n1 - n2
print(ReturnMinus(23,38))

n1=30
n2=65
def ReturnProduct(n1,n2):
    return n1*n2
print(ReturnProduct(30,65))

    